def main():
    print("나는 %d살입니다." %22) #나는 22살입니다.
    print("나는 %s살입니다." %22) #나는 22살입니다. (%s는 정수값 표현 가능)
    print("나는 %s를 좋아합니다." %"누워있기") #나는 누워있기를 좋아합니다.
    print("Apple은 %c로 시작합니다." %'A') #Apple은 A로 시작합니다.

    print("나는 %s색과 %s색을 좋아해요." %("흰", "검은")) #나는 흰색과 검은색을 좋아해요.


main()
